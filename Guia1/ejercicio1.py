"""1 - División con resto
Plantear un script (directamente en el shell de Python) que permita informar, para dos valores a y
b el resultado de la división a/b y el resto de esa división."""

a = 10
b = 5

division = a // b
resto = a % b
print(f"El resultado de la división es: {division}")
print(f"El resto de la división es: {resto}")